<script>
    import axios from "axios";
    export default {
        name: "ProductDetailsView",
        data(){
            return{
                product:{},
                id:this.$route.params.id,
                selectedColor:'',
                selectedSize:'',
                qty:1,
                successMessage: '',
            }
        },
        mounted() {
            axios.get(`http://localhost/project-works/public/api/get-product-details/${this.id}`).then(res => {
                this.product = res.data;

                if (this.product.product_colors?.length) {
                    this.selectedColor = this.product.product_colors[0].color.name;
                }
                if (this.product.product_sizes?.length) {
                    this.selectedSize = this.product.product_sizes[0].size.name;
                }
            }).catch(error => {
                console.error("Error fetching product details:", error);
            });
        },
        created() {
            this.getProductDetails()
        },
        methods:{
            addToCart(){
                const cartData ={
                    product_id : this.product.id,
                    name:        this.product.name,
                    price:       this.product.selling_price,
                    qty:         this.qty,
                    color:       this.selectedColor,
                    size:        this.selectedSize,
                    image:       this.product.image
                };

                axios.post('/api/add-to-cart', cartData).then(res=>{
                    this.successMessage = res.data.message;

                    setTimeout(() => {
                        this.successMessage = '';
                    }, 2000);

                }).catch(error =>{
                    console.error("Cart error:", error);
                    alert("Something went wrong!");
                });
            },
            getProductDetails()
            {
                axios.get("http://localhost/project-works/public/api/get-product-details/"+this.id).then((response) => {console.log(response.data)
                    this.product =response.data;
                });
            }
        }
